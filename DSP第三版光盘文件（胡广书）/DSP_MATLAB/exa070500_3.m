clear all;

% 梳状滤波器的频率响应和极零图；
M=4096;
rn1=0.96;rd1=0.98;
rn2=0.98;rd2=0.96;

b=[1 0 0 0 0 0 0 0 -rn1];
a=[1 0 0 0 0 0 0 0 -rd1];
[h,f]=freqz(b,a,M*4,'whole',1); 
subplot(221)
plot(f,abs(h));grid;

b=[1 0 0 0 0 0 0 0 -rn2];
a=[1 0 0 0 0 0 0 0 -rd2];
[h,f]=freqz(b,a,M*4,'whole',1); 
subplot(222)
plot(f,abs(h));grid;


